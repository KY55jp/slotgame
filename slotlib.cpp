/* データ循環(スクロール用) */
//std::vector<character_data_def> &cds
void rotate_slot(character_data_def *p, int size)
{
	if(size < 2) return;

	character_data_def tmp = p[size - 1];

	for(int i = size - 1; i >= 0; i--){
		if(i > 0){
			p[i] = p[i - 1];
		}else{
			p[i] = tmp;
		}

	}
}

