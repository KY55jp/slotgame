{
	/* イメージの描写 */
	for(auto &cd : cds){
		draw_character(cd.r, cd.bmp_data, scale);
	}

	draw_bg(); /* 背景の描写 */
}

//スロットが揃っているか判定
int collision_detection(std::vector<character_data_def>& cds)
{
	int result = 0;
	int slot_h = 0;

	if(cds[slot_h].char_no == cds[slot_h + slot_height].char_no //横軸がヒットしているか？
	   && cds[slot_h + slot_height].char_no == cds[slot_h + (slot_height * 2)].char_no){
		result++;
		score += 50;
		coin += 50;
	}
	slot_h++;
	if(cds[slot_h].char_no == cds[slot_h + slot_height].char_no //横軸がヒットしているか？
	   && cds[slot_h + slot_height].char_no == cds[slot_h + (slot_height * 2)].char_no){
		result++;
		score += 50;
		coin += 50;
	}
	slot_h++;
	if(cds[slot_h].char_no == cds[slot_h + slot_height].char_no //横軸がヒットしているか？
	   && cds[slot_h + slot_height].char_no == cds[slot_h + (slot_height * 2)].char_no){
		result++;
		score += 50;
		coin += 50;
	}

	slot_h = 0;
	if(cds[slot_h].char_no == cds[slot_h + slot_height + 1].char_no //斜め軸がヒットしているか？
	   && cds[slot_h + slot_height + 1].char_no == cds[slot_h + (slot_height * 2) + 2].char_no){
		result++;
		score += 50;
		coin += 50;
	}
	
	if(cds[slot_h + 2].char_no == cds[slot_h + slot_height + 1].char_no //斜め軸がヒットしているか？
	   && cds[slot_h + slot_height + 1].char_no == cds[slot_h + (slot_height * 2)].char_no){
		result++;
		score += 50;
		coin += 50;
	}
	
	return result;
}

//メインゲーム処理
int do_process(std::vector<character_data_def>& cds, int scale)
{
	static bool pause_pressed = false;
	/* ポーズキー判定 */
	if(pico_display.is_pressed(pico_display.B) && !pause_pressed){
		pause_pressed = true;
	} else if(!pico_display.is_pressed(pico_display.B) && pause_pressed){
		pause_pressed = false;
		game_status = Game_OnPause;
	}
	/* リセット判定 */
	if(pico_display.is_pressed(pico_display.A)){
		game_status = Game_Opening;
	}

	static bool pressed = false;
	/* スロットストップキー判定 */
	if(pico_display.is_pressed(pico_display.Y) && !pressed){
		pressed = true;
	} else if(!pico_display.is_pressed(pico_display.Y) && pressed){
		/* キーが押されて放された場合、ストップとする */
		pressed = false;
		/* スロットを停止フラグを立てる */
		if(stop_slot_pos < slot_height * 3){
			for(int i = 0; i < slot_height; i++){
				cds[stop_slot_pos + i].move = false;
			}
			stop_slot_pos += slot_height;
		}
	}

	bool collision_flag = false;
	int result = 0;
	for(int i = 0; i < (int)cds.size(); i++){ /* スロット座標計算 */
		if(cds[i].speed != 0) {
//test before		cds[i].r.y += cds[i].speed;
			/* スクロール（ローテーション） */
			if((i == 0 || i == slot_height || i == (slot_height * 2))){
				if(cds[i].r.y >= 0){
					rotate_slot(&cds[i], slot_height);
					cds[i].r.y = cds[i + 1].r.y - 16;
				}
			}
			cds[i].r.y += cds[i].speed; //test after
			//スロットオブジェクトのY座標が16の倍数　かつ
			//スロットが移動していない　かつ
			//スロットのスピードが１の場合
			if((cds[i].r.y % 16) ==  0 && !cds[i].move && cds[i].speed == 1) {
				cds[i].speed = 0; //移動量（スピード）を0にする

				//スロット停止インデックスが２（最後）
				if(stop_slot_pos == slot_height * 3 && !collision_flag){
					collision_flag = true;
					result = collision_detection(cds);
					if(result > 0){
						game_status = Game_OnScore;
					} else {
						coin -= 50;
						if(coin <= 0){
							game_status = Game_Over;
						} else {
							init_data(cds); //データ初期化
							pico_display.set_led(255, 0, 0);
							sleep_ms(500);
							pico_display.set_led(0, 0, 0);
							game_status = Game_OnGoing;
						}
					}
				}
			}
		}

		if(!cds[i].move){ //スロットが移動していなかった場合
			if(cds[i].speed > 1){ //移動量（スピード）が２以上の場合
				cds[i].speed--; //移動量（スピード）を減らす
			}
		}
	}

	/* ディスプレイ更新 */
	pico_display.set_pen(0, 0, 0);
	pico_display.clear();

	draw_slot(cds, scale); /* スロット描写 */
	pico_display.update(); //ディスプレイ更新

	return result;
}

void do_pause()
{
	/* ポーズキー(X)キー押下判定 */
	pico_display.set_pen(255, 0, 0);
	pico_display.text("PAUSE!", Point(90, 60), 240, 3);
	pico_display.text("PRESS X TO CONTINUE", Point(0, 80), 240, 2);
	pico_display.update();

	while(!pico_display.is_pressed(pico_display.X)){}

	game_status = Game_OnGoing;
}
