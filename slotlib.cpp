/* スロット柄をシャッフル */
void shuffle_slot(int* data, int size)
{
	/* まず最初に初期値を設定する */
	for(int i = 0; i < size; i++){
		data[i] = i;
	}
	/* シャッフル */
	for(int i = 0; i < 90; i++){
		long int r = random() % size;
		long int r2 = random() % size;
		int tmp = data[r];
		data[r] = data[r2];
		data[r2] = tmp;
	}
}

/* データ初期化 */
void init_data(std::vector<character_data_def>& cds)
{
	static const int x_pos[] = {2, 22, 42};
	static int char_num[9];

	/* すでにデータがセットされている場合、データを削除 */
	if(!cds.empty()){
		cds.clear();
	}

	static int last_score = 0;
	if(score >= 100 && start_speed < 7 && score != last_score){
		start_speed++;
		last_score = score;
	}
	/* データ初期化 */
	int loop_cnt = sizeof x_pos / sizeof(x_pos[0]); //3

	for(int j = 0; j < loop_cnt; j++){
		int slot_speed_adj = random() % 2; /* スロットスピード調整 */
		shuffle_slot(char_num, 9); /* ビットマップキャラのシャッフル */
		for(int i = 0; i < slot_height; i++){
			character_data_def cd;
			cd.r = Rect(x_pos[j], (i * 16), 16, 16);
			cd.speed = start_speed + slot_speed_adj;
			cd.move = true;
			cd.bmp_data = (uint16_t *)&char_data[char_num[i] * 16 * 16];
			cd.char_no = char_num[i];
			cds.push_back(cd);
		}
	}
	stop_slot_pos = 0;
}

//スロット描写
